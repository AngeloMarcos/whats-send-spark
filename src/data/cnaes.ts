export function searchCNAEs(query: string, limit = 20): CNAE[] {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  
  return cnaes
    .filter(cnae => {
      const normalizedCode = cnae.code.toLowerCase();
      const normalizedName = cnae.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
      return normalizedCode.includes(normalizedQuery) || normalizedName.includes(normalizedQuery);
    })
    .slice(0, limit);
}

export function getCNAEsByCategory(categoryCode: string): CNAE[] {
  return cnaes.filter(cnae => cnae.category === categoryCode);
}

export function getCNAEByCode(code: string): CNAE | undefined {
  return cnaes.find(cnae => cnae.code === code);
}
